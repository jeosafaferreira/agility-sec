    private void listData() {
        try {
            rs = con.prepareStatement("SELECT YEAR(venc) FROM finances GROUP BY YEAR(venc)").executeQuery();
            while (rs.next()) {
                cboAno.addItem(rs.getString("YEAR(venc)"));
            }
            cboAno.addItem("<HTML><B>TODOS");
            //SELECIONA DATA ATUAL
            cboMes.setSelectedIndex(cal.get(Calendar.MONTH));
            cboAno.setSelectedItem("" + cal.get(Calendar.YEAR));
        } catch (SQLException e) {
            AgilitySec.showError(this, "#1131", e);
        }
    }

    private boolean check() {
        boolean r = false;

        if (cboMes.getSelectedIndex() > -1) {
            if (cboAno.getSelectedIndex() > -1) {
                r = true;
            } else {
                AgilitySec.checkMessageError(this, cboAno, "ANO");
            }
        } else {
            AgilitySec.checkMessageError(this, cboMes, "MÊS");
        }
        return r;
    }

    /**
     *
     * @return "2018; 01; JANEIRO"
     */
    public String getData() {
        String ano, mesStr;
        int mesInt;

        ano = cboAno.getSelectedItem().toString();
        mesInt = cboMes.getSelectedIndex() + 1;
        mesStr = cboMes.getSelectedItem().toString();

        return ano + ";" + mesInt + ";" + mesStr;
    }

