    public DataSelec(String opt) {
        initComponents();
        con = ModuloConexao.conector();
        getRootPane().setDefaultButton(btnCon);
        this.opt = opt;
        switch (opt) {
            case "DisableMonth":
                cboMes.setSelectedIndex(12);
                cboMes.setEnabled(false);
                break;
            case "DisableOptionAll": {
                cboMes.removeItem("<HTML><B>TODOS");
                cboAno.removeItem("<HTML><B>TODOS");
                break;
            }
        }
        listData();
    }

